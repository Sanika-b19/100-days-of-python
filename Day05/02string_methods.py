# string methods in python


example_string = "Hello, World!"
print("String Methods:")
print(example_string.lower())     # Convert to lowercase
print(example_string.upper())     # Convert to uppercase
print(example_string.replace("World", "Python"))  # Replace substring
print(example_string.find("World"))  # Find the position of substring
print(example_string.count("l"))   # Count occurrences of a character
