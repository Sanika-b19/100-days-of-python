# Basic number manipulation in Python
num = 10

# Increment
num += 1  # num is now 11
print("Incremented number:", num)

# Decrement
num -= 2  # num is now 9
print("Decremented number:", num)

# Multiplication
num *= 3  # num is now 27
print("Multiplied number:", num)

# Division
num /= 3  # num is now 9.0
print("Divided number:", num)

# Power
num = num ** 2  # num is now 81.0
print("Number raised to power:", num)

# Rounding numbers
print("Rounded number:", round(num))       # 81
print("Rounded to 2 decimal places:", round(num, 2))  # 81.0
